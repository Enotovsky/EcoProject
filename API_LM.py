import json
from openai import OpenAI


class LMStudioError(Exception):
    """Исключение при обращении к серверу LM Studio (соединение, отсутствие моделей и т.д.)"""
    pass


class LLMResponseError(Exception):
    """Исключение при некорректном формате ответа от модели (например, не JSON)"""
    pass


class EcoScanAI:
    def __init__(self, base_url="http://localhost:1234/v1", api_key="lm-studio"):
        # Инициализируем клиент, указывая адрес локального сервера LM Studio
        self.client = OpenAI(base_url=base_url, api_key=api_key)

        # Задаем тот самый системный промт, который мы протестировали
        self.system_prompt = (
            "Ты — строгий API для платформы EcoScan. "
            "Твоя задача — проанализировать список товаров из чека и вернуть структурированный JSON-массив.\n\n"
            "СТРОГИЕ ПРАВИЛА ВЫВОДА:\n"
            "1. Возвращай ТОЛЬКО валидный JSON-массив. Никаких приветствий, никакого Markdown (не оборачивай в ```json), никаких пояснений. Твой ответ должен начинаться с '[' и заканчиваться на ']'.\n"
            "2. Категории и CO2: Каждому товару назначь ОДНУ из следующих категорий и её точный коэффициент CO2:\n"
            "   - 'Мясо и Птица' (CO2: 15.0)\n"
            "   - 'Молочные продукты и Яйца' (CO2: 4.5)\n"
            "   - 'Рыба и Морепродукты' (CO2: 5.0)\n"
            "   - 'Овощи, Фрукты и Зелень' (CO2: 0.6)\n"
            "   - 'Бакалея и Хлеб' (CO2: 1.2)\n"
            "   - 'Сладости и Снеки' (CO2: 2.0)\n"
            "   - 'Напитки в пластике/жести' (CO2: 1.5)\n"
            "   - 'Пластиковая упаковка / Пакеты' (CO2: 3.0)\n"
            "   - 'Другое' (CO2: 1.0)\n\n"
            "3. Конкретные Эко-советы (eco_tip): В поле 'eco_tip' напиши короткий, но МАКСИМАЛЬНО КОНКРЕТНЫЙ совет.\n"
            "   - ЗАПРЕЩАЕТСЯ писать общие фразы вроде 'отличный эко-выбор', 'выбирайте органическое', 'перейдите на эко-альтернативы'.\n"
            "   - НАЗЫВАЙ ТОЧНЫЕ ПРОДУКТЫ-АНАЛОГИ. Например, для коровьего молока пиши: 'Замените на овсяное или соевое молоко (след ниже в 3 раза)'. Для свинины: 'Замените на птицу (курица, индейка) или тофу'. Для пакета: 'Используйте тканевый шоппер'.\n"
            "   - Для хороших товаров (овощи) пиши конкретику: 'Покупайте сезонные овощи на развес в свои мешочки-фруктовки вместо фасованных в пластик'.\n"
            "   - Каждый совет должен быть уникальным.\n\n"
            "Схема JSON-объекта:\n"
            "{\n"
            "  \"raw_name\": \"Оригинальная строка чека\",\n"
            "  \"clean_name\": \"Понятное очищенное название продукта на русском\",\n"
            "  \"category\": \"Точная категория из списка выше\",\n"
            "  \"co2_coefficient\": Число с плавающей точкой (коэффициент),\n"
            "  \"eco_tip\": \"Точный совет с названием продукта-аналога\"\n"
            "}\n\n"
            "ПРИМЕР ВВОДА:\n"
            "1. ПАКЕТ-МАЙКА БОЛ. 15.00\n"
            "2. Колбаса Вареная 250г 150.00\n\n"
            "ПРИМЕР ВЫВОДА:\n"
            "[\n"
            "  {\n"
            "    \"raw_name\": \"1. ПАКЕТ-МАЙКА БОЛ. 15.00\",\n"
            "    \"clean_name\": \"Пакет-майка большой\",\n"
            "    \"category\": \"Пластиковая упаковка / Пакеты\",\n"
            "    \"co2_coefficient\": 3.0,\n"
            "    \"eco_tip\": \"Откажитесь от пластиковых пакетов на кассе — носите с собой складной тканевый шоппер.\"\n"
            "  },\n"
            "  {\n"
            "    \"raw_name\": \"2. Колбаса Вареная 250г 150.00\",\n"
            "    \"clean_name\": \"Колбаса вареная 250г\",\n"
            "    \"category\": \"Мясо и Птица\",\n"
            "    \"co2_coefficient\": 15.0,\n"
            "    \"eco_tip\": \"Мясное производство вредит планете. Попробуйте веганскую колбасу из сейтана или замените на постную ветчину из индейки.\"\n"
            "  }\n"
            "]\n\n"
            "Проанализируй текст чека пользователя и верни ТОЛЬКО JSON-массив:"
        )

    def analyze_receipt_text(self, raw_text: str) -> list:
        """Отправляет текст чека в LM Studio и возвращает структурированный массив"""
        try:
            response = self.client.chat.completions.create(
                model="local-model",  # Сервер LM Studio проигнорирует имя и вызовет активную модель
                messages=[
                    {"role": "system", "content": self.system_prompt},
                    {"role": "user", "content": raw_text}
                ],
                temperature=0.2,  # Низкая температура для строгости ответов
            )

            # Получаем сырой текстовый ответ (JSON-строку) от нейросети
            json_string = response.choices[0].message.content.strip()

            # Превращаем JSON-строку в обычный список Python
            parsed_data = json.loads(json_string)
            if not isinstance(parsed_data, list):
                raise LLMResponseError("Модель вернула некорректный ответ (должен быть JSON-список).")
            return parsed_data

        except json.JSONDecodeError as e:
            raise LLMResponseError(
                f"Не удалось распознать структуру ответа нейросети как JSON. "
                f"Попробуйте отправить чек повторно. Подробности: {e}"
            )
        except Exception as e:
            error_msg = str(e)
            # Если LM Studio возвращает ошибку о том, что модели не загружены
            if "No models loaded" in error_msg:
                raise LMStudioError(
                    "В LM Studio не загружена модель! Откройте интерфейс LM Studio и выберите/загрузите модель "
                    "(например, Qwen 2.5), либо выполните команду 'lms load qwen2.5-14b-instruct' в терминале."
                )
            # Если сервер вообще выключен или недоступен
            elif "Connection error" in error_msg or "Failed to establish a new connection" in error_msg:
                raise LMStudioError(
                    "Не удалось подключиться к LM Studio на порту 1234. Убедитесь, что программа LM Studio запущена."
                )
            raise LMStudioError(f"Ошибка при работе с LM Studio: {error_msg}")


# --- ПРИМЕР ИСПОЛЬЗОВАНИЯ В БЭКЭНДЕ ---
if __name__ == "__main__":
    # 1. Создаем экземпляр нашего ИИ-модуля
    ai_module = EcoScanAI()

    # 2. Имитируем текст, полученный от OCR-распознавателя чека
    extracted_ocr_text = "1. Мол.Дом.в дер.3.2% 99.90\n2. ПАКЕТ-МАЙКА БОЛ. 15.00"

    # 3. Отправляем в модель
    print("Отправка запроса в локальную модель Qwen 2.5...")
    result = ai_module.analyze_receipt_text(extracted_ocr_text)

    # 4. На выходе получили чистые данные, с которыми бэкэнд может работать!
    print("\nРезультат успешно получен бэкэндом:")
    for item in result:
        print(f"Товар: {item['clean_name']} | Категория: {item['category']} | CO2: {item['co2_coefficient']} | Рекомендация: {item['eco_tip']}")